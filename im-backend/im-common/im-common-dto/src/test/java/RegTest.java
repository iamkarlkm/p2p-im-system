    private static void extractChapterComment(String fullContent) {


        // 查找分隔符 "---"
        int separatorIndex = fullContent.indexOf("---");
        if (separatorIndex == -1) {
            // 没有分隔符，返回原内容
            System.out.println("没有分隔符，返回原内容");
            return;
        }

        // 查找注释标记 "**【章节注释】**"
        String afterSeparator = fullContent.substring(separatorIndex + 3).trim();
        int commentMarkerIndex = afterSeparator.indexOf("**【章节注释】**");

        if (commentMarkerIndex == -1) {
            // 有分隔符但没有注释标记，返回原内容
            System.out.println("有分隔符但没有注释标记，返回原内容");
            return;
        }

        // 提取正文（分隔符之前）
        String content = fullContent.substring(0, separatorIndex).trim();

        // 提取注释（注释标记之后）
        String comment = afterSeparator.substring(commentMarkerIndex).trim();

        //log.info("[章节注释] 提取成功: 正文字数={}, 注释字数={}", content.length(), comment.length());

        System.out.println("提取正文（分隔符之前）:\n"+content);
        System.out.println("提取注释（注释标记之后）:\n"+comment);
    }

